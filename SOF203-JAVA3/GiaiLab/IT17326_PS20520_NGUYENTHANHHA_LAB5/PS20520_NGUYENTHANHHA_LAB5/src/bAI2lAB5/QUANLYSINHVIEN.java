      public boolean checkform() {
        boolean kq = true;
        if (txtmasv.getText().equals("")) {
            checkmasv.setText("Vui lòng nhập mã sinh viên");
            kq = false;
        }
          else{
             checkmasv.setText("");
        }
        if (txthoten.getText().equals("")) {
            checkhoten.setText("Vui lòng nhập họ tên");
            kq = false;
        }
          else{
             checkhoten.setText("");
        }
        if (txtemail.getText().equals("")) {
            checkmail.setText("Vui lòng nhập email");
            kq = false;
        }
          else{
             checkmail.setText("");
        }
        if (txtsodt.getText().equals("")) {
            checksdt.setText("Vui lòng nhập số điện thoại ");
            kq = false;
        }
          else{
             checksdt.setText("");
        }
        if (rdonam.isSelected() == false && rdonu.isSelected() == false) {
            checkgioitinh.setText("Vui lòng chọn giới tính");
            kq = false;
        }
          else{
             checkgioitinh.setText("");
        }
        if (txtdiachi.getText().equals("")) {
            checkdiachi.setText("Vui lòng nhập địa chỉ");
            kq = false;
        }
        else{
             checkdiachi.setText("");
        }
        return kq;
    }
