    public boolean checkform() {
        if (txxfirstnumber.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập số thứ nhất");
            return false;
        } else if (txtsecondnumber.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập số thứ 2");
            return false;
        }
        if(!txxfirstnumber.getText().matches(renumber)){ 
            System.out.println(txxfirstnumber.getText());
             JOptionPane.showMessageDialog(this, "Vui lòng nhập dạng số cho số thứ 1");
            return false;
        }
        else if(!txtsecondnumber.getText().matches(renumber)){ 
             JOptionPane.showMessageDialog(this, "Vui lòng nhập dạng số cho số thứ 2");
            return false;
        }
        return true;
    }

