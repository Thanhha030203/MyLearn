    public void table() {
        model = new DefaultTableModel();
        model.setColumnIdentifiers(header);
        jTable1.setModel(model);
        model.setRowCount(0);
        for (int i = 0; i < list.size(); i++) {
            model.addRow(new Object[]{list.get(i).getName(), list.get(i).getStandard()});
        }
        model.fireTableDataChanged();

    }

