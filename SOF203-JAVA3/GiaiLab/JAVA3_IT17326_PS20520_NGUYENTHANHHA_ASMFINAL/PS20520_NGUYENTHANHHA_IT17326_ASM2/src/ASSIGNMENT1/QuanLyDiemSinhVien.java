    public boolean checkdiem() {
        if ((!txttenganh.getText().matches(rediem)) || (!txttinhoc.getText().matches(rediem)) || (!txtgdtc.getText().matches(rediem))) {
            return false;
        } else {
            return true;
        }
    }
