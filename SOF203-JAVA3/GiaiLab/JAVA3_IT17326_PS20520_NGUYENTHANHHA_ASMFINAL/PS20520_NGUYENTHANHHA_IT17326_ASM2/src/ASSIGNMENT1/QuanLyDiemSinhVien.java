    public void addrow() throws SQLException {

        model.setRowCount(0);
        for (int i = 0; i < list.size(); i++) {
            Students std = list.get(i);
            double trungbinh = (std.getDiemtienganh() + std.getDiemgdtc() + std.getDiemtinhoc()) / 3;
            model.addRow(new Object[]{std.getMASV(), std.getHoten(), std.getDiemtienganh(), std.getDiemtinhoc(), std.getDiemgdtc(), trungbinh});
        }
        model.fireTableDataChanged();
    }

