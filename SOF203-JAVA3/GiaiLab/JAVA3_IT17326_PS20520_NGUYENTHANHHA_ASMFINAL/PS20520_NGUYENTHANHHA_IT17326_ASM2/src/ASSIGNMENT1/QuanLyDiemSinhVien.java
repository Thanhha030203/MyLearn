        txttinhoc.setText(String.valueOf(std.getDiemtinhoc()));
        txtgdtc.setText(String.valueOf(std.getDiemgdtc()));
        double diemtb = (std.getDiemtinhoc() + std.getDiemtienganh() + std.getDiemgdtc()) / 3;

