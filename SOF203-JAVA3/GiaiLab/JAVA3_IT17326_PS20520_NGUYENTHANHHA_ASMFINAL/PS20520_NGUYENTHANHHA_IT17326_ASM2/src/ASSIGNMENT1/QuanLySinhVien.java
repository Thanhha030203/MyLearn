    public boolean check() {
        boolean ktra = true;
        if (txtmasv.getText().equals("")) {
            s = s + " Mã sinh viên ;";
            ktra = false;
        }
        if (txthoten.getText().equals("")) {
            s = s + " Họ tên sinh viên";
            ktra = false;
        }
        if (!rdonam.isSelected() && !rdonu.isSelected()) {
            s = s + " chọn giới tính";
            ktra = false;
        }

        return ktra;
    }

    public boolean checkmasv() {
        for (int i = 0; i < list.size(); i++) {
            if (list.get(i).getMASV().equalsIgnoreCase(txtmasv.getText())) {
                JOptionPane.showMessageDialog(null, "Đã tồn tại mã sinh viên");
                return false;
            }
        }
        return true;
    }

