    private void btnupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnupdateActionPerformed
        // TODO add your handling code here:
        int row = table.getSelectedRow();
        if (check()) {
            if (checform()) {
                byte gt = -1;
                if (rdonam.isSelected()) {
                    gt = 1;
                } else {
                    gt = 0;
                }
                if (nameimg == null) {
                    nameimg = list.get(row).getHinh();
                }

                String update = "update STUDENTS SET Hoten = N'" + txthoten.getText() + "', Email = '" + txtemail.getText() + "', SoDT = '" + txtsdt.getText() + "', Gioitinh =" + gt + ", Diachi = N'" + txtdiachi.getText() + "', Hinh = N'" + nameimg + "' where MASV = '" + txtmasv.getText() + "';";
                try {
                    con.getResult(update);
                    JOptionPane.showMessageDialog(null, "Cập nhật thành công");
                    list = con.getResultqury(sql);
                    table();
                    addrow();
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(null, "Cập nhật thất bại");
                    Logger.getLogger(QuanLySinhVien.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                JOptionPane.showMessageDialog(null, r);
                r = "Vui lòng nhập đúng : ";
            }
        } else {
            JOptionPane.showMessageDialog(null, s);
            s = "Vui lòng nhập : ";
        }
        nameimg = null;
    }//GEN-LAST:event_btnupdateActionPerformed

