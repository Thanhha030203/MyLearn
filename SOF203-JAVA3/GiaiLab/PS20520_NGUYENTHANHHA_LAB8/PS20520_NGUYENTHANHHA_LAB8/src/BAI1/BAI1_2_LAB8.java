    private void btnsendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsendActionPerformed
        // TODO add your handling code here:
        try {
            Properties p = new Properties();
            p.put("mail.smtp.auth", "true");
           p.put("mail.smtp.starttls.enable", "true");
           p.put("mail.smtp.host", "smtp.gmail.com");
           p.put("mail.smtp.port", 587);
           String accountName = txtfrom.getText();
           String accountPassword = txtpass.getText();
           Session s = Session.getInstance(p, new javax.mail.Authenticator(){ 

                @Override
                protected PasswordAuthentication getPasswordAuthentication() {
                    return new PasswordAuthentication( accountName,accountPassword); //To change body of generated methods, choose Tools | Templates.
                }
        
           });
           Message msg = new MimeMessage(s);
           msg.setFrom(new InternetAddress(txtfrom.getText()));
           msg.setRecipients(Message.RecipientType.TO,InternetAddress.parse(txtTo.getText()));
           if(!txtbcc.getText().equals("")){
           msg.setRecipients(Message.RecipientType.BCC,InternetAddress.parse(txtbcc.getText()));
           }
           if(!txtcc.getText().equals("")){
           msg.setRecipients(Message.RecipientType.CC,InternetAddress.parse(txtcc.getText()));
           }
           msg.setSubject(txtsubject.getText());
           msg.setText(txttext.getText());
           if(!txtfile.getText().equals("")){
          MimeBodyPart contentPart = new  MimeBodyPart();
          contentPart.setContent(txttext.getText(),"text/plain");
          Multipart mp = new MimeMultipart();
          mp.addBodyPart(contentPart);
          MimeBodyPart filePart = new MimeBodyPart();

           FileDataSource source = new FileDataSource(txtfile.getText());
           filePart.setDataHandler(new DataHandler(source));
           filePart.setFileName(source.getName());
        
           mp.addBodyPart(filePart);
           msg.setContent(mp);
        }
            Transport.send(msg);
            JOptionPane.showMessageDialog(null, "Gá»­i thÃ nh cÃ´ng","Message",JOptionPane.INFORMATION_MESSAGE);
           
        } catch (Exception e) {
            System.out.println("lá»i"+ e.getLocalizedMessage() );
        }
    }//GEN-LAST:event_btnsendActionPerformed

