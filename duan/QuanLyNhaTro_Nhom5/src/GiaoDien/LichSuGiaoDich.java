    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        giaodich = gdd.Select_ID(txtSearch.getText());
        if (giaodich == null) {
            JOptionPane.showMessageDialog(null, "Không tìm thấy thông tin giao dịch");
        } else {
            for (int i = 0; i < list.size(); i++) {
                if (giaodich.getMaGD().equals(list.get(i).getMaGD())) {
                    index = i;
                    try {
                        JOptionPane.showMessageDialog(null, "Đã tìm thấy");
                        tblBang.setRowSelectionInterval(index, index);
                        duyetmang(index);
                    } catch (ParseException ex) {
                        Logger.getLogger(LichSuGiaoDich.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            }
        }

    }//GEN-LAST:event_btnSearchActionPerformed

