    void fillComboBoxLoai() {
        int kq = cboloaisp.getSelectedIndex();
        String loai = (kq == 0) ? "Bánh Kẹo" : "Rượu Bia";
        DefaultTableModel model = (DefaultTableModel) bang.getModel();
       
        model.setRowCount(0);
        try {
            String sql = "SELECT * FROM sanpham WHERE loaisp like ?";
            List<SanPham> list = (List<SanPham>) dao.selectBySql(sql, loai);
            for (SanPham cd : list) {
                Object[] row = {
                    cd.getMasp(),
                    cd.getTensp(),
                    cd.getDongia(),
                    cd.getLoaisp()
                };
                model.addRow(row);
            }
        } catch (Exception a) {
            System.out.println("lỗitaij 193 " + a);
        }
    }

