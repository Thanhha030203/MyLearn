    SanPhamdao dao = new SanPhamdao();

    void fillTable() {
        DefaultTableModel model = (DefaultTableModel) bang.getModel();
        model.setRowCount(0);
        try {
            List<SanPham> list = dao.selectAll();
            for (SanPham cd : list) {
                Object[] row = {
                    cd.getMasp(),
                    cd.getTensp(),
                    cd.getDongia(),
                    cd.getLoaisp()
                };
                model.addRow(row);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Lỗi");
        }
    }

    void fillTableSL() {
        DefaultTableModel model = (DefaultTableModel) table2.getModel();
        model.setRowCount(0);
        List<Object[]> list = dao.getSL();
        for (Object[] row : list) {
            model.addRow(row);
        }
    }


