    public void duyetmang(int index) {
        txtMaNha.setText(list.get(index).getMaNT());
        txtTenNha.setText(list.get(index).getTenNT());
        txtNgLienHe.setText(list.get(index).getMa_NLH());
        txtDienTich.setText(String.valueOf(list.get(index).getDientich()));
        txtDiaChi.setText(list.get(index).getDiaChi());
        txtGiaPhong.setText(String.valueOf(list.get(index).getGia()));
        txtmota.setText(list.get(index).getMota());
        cboTrangThai.setSelectedItem(list.get(index).isTrangThai());
        cboChungCu.setSelectedItem(list.get(index).getTenLoai().equals(cboChungCu.getSelectedItem()));
    }

    public NhaTro addUpdate() {
        nt = new NhaTro();
        nt.setTenNT(txtTenNha.getText());
        nt.setMaNT("0000");
        nt.setDiaChi(txtDiaChi.getText());
        nt.setMa_NLH(user.getMaTK());
        nt.setGia(Double.parseDouble(txtGiaPhong.getText()));
        nt.setTenNT(txtTenNha.getText());
        nt.setTrangThai((cboTrangThai.getSelectedIndex() == 1) ? true : false);
        nt.setTenLoai((String) cboChungCu.getSelectedItem());
        String sql2 = "Select * from LoaiNhaTro where TenLoai like N'%" + cboChungCu.getSelectedItem() + "%'";
        LoaiNhaTro lnt = new LoaiNhaTro();
        lnt = new Connections().getLoaiNhaTro(sql2);
        System.out.println("loai trọ " + lnt.getMalOai());
        nt.setMaLoai(lnt.getMalOai());
        nt.setDientich(Integer.parseInt(txtDienTich.getText()));
        nt.setMota(txtmota.getText());
        nt.setHinh("ok");
        return nt;
    }

