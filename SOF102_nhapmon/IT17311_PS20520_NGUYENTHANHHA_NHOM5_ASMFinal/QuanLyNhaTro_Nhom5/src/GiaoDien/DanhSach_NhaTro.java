    public void addRow() {
        String select = "Select * from NhaTro n inner join LoaiNhaTro l on n.MaLoai = l.MaLoai and MA_NLH = '" + user.getMaTK() + "'";
        try {
            list = new Connections().getNhaTro_LoaiTro1(select);
            System.out.println("kist : " + list.size());
        } catch (SQLException ex) {
            Logger.getLogger(DanhSach_NhaTro.class.getName()).log(Level.SEVERE, null, ex);
        }
        model.setRowCount(0);
        for (NhaTro n : list) {
            System.out.println(n);
            String tt = (n.isTrangThai()) ? "Còn phòng" : "Hết phòng";
            model.addRow(new Object[]{n.getMaNT(), n.getTenNT(), n.getDiaChi(), n.getGia(), n.getDientich(), n.getMota(), n.getMa_NLH(), n.getHinh(), tt, n.getTenLoai()});
        }
        model.fireTableDataChanged();
    }

