    void fillTableThongKebenhnhan() {
        DefaultTableModel model = (DefaultTableModel) thongke_benhnhan.getModel();
        model.setRowCount(0);
        List<Object[]> list = dao.getTKBenhNhan();
        for (Object[] row : list) {
            model.addRow(row);
        }
    }

    void fillTableThongKebenhnhantheoMA(String MA) {
        DefaultTableModel model = (DefaultTableModel) thongkebenhnhan_chitiet.getModel();
        model.setRowCount(0);
        List<Object[]> list = dao.getTKBenhNhan_TheoMA(MA);
        for (Object[] row : list) {
            model.addRow(row);
        }
    }
        void fillTableThongKebenhnhantheothang(String thang,String nam) {
        DefaultTableModel model = (DefaultTableModel) thongkebenhnhan_chitiet.getModel();
        model.setRowCount(0);
        List<Object[]> list = dao.getTKBenhNhan_TheoThangNam(thang, nam);
        for (Object[] row : list) {
            model.addRow(row);
        }
    }
            void fillTableThongKebenhnhantheonam(String nam) {
        DefaultTableModel model = (DefaultTableModel) thongkebenhnhan_chitiet.getModel();
        model.setRowCount(0);
        // int thang = (Integer)cboThang.getSelectedItem();
        List<Object[]> list = dao.getTKBenhNhan_TheoThang(nam);
        for (Object[] row : list) {
            model.addRow(row);
        }
    }
         public void select() {
        thongke_benhnhan.clearSelection();
        if (cbothang.getSelectedIndex() != 0 && cbonam.getSelectedIndex() != 0) {
            DefaultTableModel model = (DefaultTableModel)thongke_benhnhan.getModel();
            model.setRowCount(0);
            List<Object[]> list = dao.getTKBenhNhan_TheoThangNam((String) cbothang.getSelectedItem(), (String) cbonam.getSelectedItem());
            for (Object[] row : list) {
                model.addRow(row);
            }
        } else if (cbonam.getSelectedIndex() != 0) {
            DefaultTableModel model = (DefaultTableModel) thongke_benhnhan.getModel();
            model.setRowCount(0);
            List<Object[]> list = dao.getTKBenhNhan_TheoThang((String) cbonam.getSelectedItem());
            for (Object[] row : list) {
                model.addRow(row);
            }
        }
        else{ 
            fillTableThongKebenhnhan();
        }
    }
     

     
