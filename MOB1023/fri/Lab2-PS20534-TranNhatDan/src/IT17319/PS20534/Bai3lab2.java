    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        String username = txtUsername.getText();
        String pass = txtPassword.getText();
        if (username.equals("")) {
            txtUsername.requestFocus();
            JOptionPane.showMessageDialog(this, "Username khong duoc de trong");
        } else if (pass.equals("")) {
            JOptionPane.showMessageDialog(this, "Pass word khong duoc de trong");
        } else if (username.equals("fpt") && pass.equals("polytechnic")) {
            if (chkRemember.isSelected()) {
                JOptionPane.showMessageDialog(this, "Dang nhap thanh cong tai khoan cua ban da duoc ghi nho");
            } else {
                JOptionPane.showMessageDialog(this, "Dang nhap thanh cong");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Username hoac password khong dung");
        }
        // end btnLogin 
    }//GEN-LAST:event_btnLoginActionPerformed

