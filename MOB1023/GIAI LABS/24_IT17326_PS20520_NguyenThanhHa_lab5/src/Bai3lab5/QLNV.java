    public void addRow(){ 
        model = (DefaultTableModel) jtable.getModel();
        model.setRowCount(0);
        for(Staff st : list){
            model.addRow(new Object[] {st.fullname,st.salary});
        
        }
    }
    public void addStaff(){ 
        Staff staff = new Staff();
        staff.fullname = txthoten.getText();
        staff.salary = Double.parseDouble(txtluong.getText());
        list.add(staff);
    }
