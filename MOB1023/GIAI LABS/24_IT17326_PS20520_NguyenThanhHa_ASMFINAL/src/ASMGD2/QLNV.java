    public void saveform() {
        int count = 0;
        try {
            if (!txtmanv.getText().equals("") && !txthonv.getText().equals("") && !txtmail.equals("") && !txtluong.getText().equals("") && !txttuoi.getText().equals("")) {
                if (checkform()) {
                    if (ds.size() == 0) {
                        if (19 <= Integer.parseInt(txttuoi.getText()) && 55 >= Integer.parseInt(txttuoi.getText())) {
                            if (Double.parseDouble(txtluong.getText()) >= 5000000) {
                                NhanVien nv = new NhanVien();
                                nv.setManv(Integer.parseInt(txtmanv.getText()));
                                 nv.setHoten(txthonv.getText());
                                  nv.setTuoi(Integer.parseInt(txttuoi.getText()));
                                    nv.setMail(txtmail.getText());
                                    nv.setLuong(txtluong.getText());
                                ds.add(nv);
                                JOptionPane.showMessageDialog(this, "Successfully added employees");
                            } else {
                                JOptionPane.showMessageDialog(rootPane, "Salary must than be more 5000000");
                            }
                        } else {
                            JOptionPane.showMessageDialog(this, "Age from 19 to 55");
                        }
                    } else {
                        for (NhanVien stt1 : ds) {
                            if (txtmanv.getText().equalsIgnoreCase(String.valueOf(stt1.getManv()))) {
                                JOptionPane.showMessageDialog(this, "Code staff already exists");
                                count = 0;
                                break;
                            } else {
                                count = 1;
                            }
                        }
                        if (count == 1) {

                            if (19 <= Integer.parseInt(txttuoi.getText()) && 55 >= Integer.parseInt(txttuoi.getText())) {
                                NhanVien nv = new NhanVien(Integer.parseInt(txtmanv.getText()), txthonv.getText(), Integer.parseInt(txttuoi.getText()), txtmail.getText(), Double.parseDouble(txtluong.getText()));
                                ds.add(nv);
                                JOptionPane.showMessageDialog(this, "Successfully added employees");
                            } else {
                                JOptionPane.showMessageDialog(this, "Age from 19 to 55");
                            }
                        } else {
                            JOptionPane.showMessageDialog(this, "Add failed employees");
                        }
                    }

                } else {
                    if (!txtmanv.getText().matches(remanv)) {
                        JOptionPane.showMessageDialog(rootPane, "Please enter the Employee Code as a number");
                    }
                    if (!txttuoi.getText().matches(retuoi)) {
                        JOptionPane.showMessageDialog(rootPane, "Please enter Age as a number");
                    }
                    if (!txtluong.getText().matches(reluong)) {
                        JOptionPane.showMessageDialog(rootPane, "Please enter Salary as a number");
                    }
                    if (!txtmail.getText().matches(remail)) {
                        JOptionPane.showMessageDialog(rootPane, "Please enter Email in correct format");
                    }
                }
            } else {
                JOptionPane.showMessageDialog(this, "Please enter full information");
            }

        } catch (Exception e) {
            System.out.println("Error from save form" + e);
        }
    }

