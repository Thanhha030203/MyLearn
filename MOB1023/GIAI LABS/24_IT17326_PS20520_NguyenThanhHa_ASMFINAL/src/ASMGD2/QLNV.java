    public void addRow() {
        while (model.getRowCount() > 0) {
            model.removeRow(0);
        }
        for (NhanVien nv : ds) {
            model.addRow(new Object[]{String.valueOf(nv.getManv()), nv.getHoten(), String.valueOf(nv.getTuoi()), nv.getMail(), String.valueOf(nv.getLuong())});
        }
        model.fireTableDataChanged();
    }

    public void writeform(int i) {
        txtmanv.setText(String.valueOf(ds.get(i).getManv()));
        txtmanv.setEnabled(false);
        txthonv.setText((String) ds.get(i).getHoten());
        txttuoi.setText(String.valueOf(ds.get(i).getTuoi()));
        txtmail.setText((String) ds.get(i).getMail());
        txtluong.setText(String.valueOf(ds.get(i).getLuong()));
        jtable.setRowSelectionInterval(i, i);
        uprecord(i);
    }

    public void uprecord(int i) {
        if (jtable.getRowCount() == 0) {
            txtrecord.setText("Record : none row in table");
        } else {
            txtrecord.setText("Record : " + (i + 1) + " of " + ds.size());
        }
    }

