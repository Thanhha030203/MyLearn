    public void loadSPToList() {
        SanPham sp1 = new SanPham("SP1", "Samsung Galaxy A31", 6200000);
        SanPham sp2 = new SanPham("SP2", "Samsung Galaxy Note 10", 16000000);
        SanPham sp3 = new SanPham("SP3", "Samsung Galaxy A80", 8900000);
        
        listSPBan.add(sp1);
        listSPBan.add(sp2);
        listSPBan.add(sp3);
        
        DefaultListModel model = new DefaultListModel();
        for(SanPham sp : listSPBan) {
            model.addElement(sp.getTensp());
        }
        lst_DanhSachSP.setModel(model);
    }
    
    public void fillToTable() {
        DefaultTableModel model = (DefaultTableModel) tbl_SPGioHang.getModel();
        model.setRowCount(0);
        Set<String> keyset = giohang.getChiTietGioHang().keySet();
        for(String key : keyset) {
            SanPham sp = giohang.getChiTietGioHang().get(key).getSp();
            int soluong = giohang.getChiTietGioHang().get(key).getSoluong();
            Object[] row = {sp.getMasp(), sp.getTensp(), formatter.format(new BigDecimal(sp.getDongia())) + " VND", soluong};
            model.addRow(row);
        }
    }
    
    public void selectSP() {
        int index = lst_DanhSachSP.getSelectedIndex();
        txt_masp.setText(listSPBan.get(index).getMasp());
        txt_TenSP.setText(listSPBan.get(index).getTensp());
        txt_Dongia.setText(formatter.format(new BigDecimal(listSPBan.get(index).getDongia())) + " VND");
    }
    
    public void addSP() {
        int index = lst_DanhSachSP.getSelectedIndex();
        SanPham sp = listSPBan.get(index);
        int soluong = Integer.parseInt(txt_Soluongmua.getText());
        SanPhamDTO spDTO = new SanPhamDTO(sp, soluong);
        giohang.addProduct(spDTO);
        fillToTable();
    }
    
    public void removeSP() {
        int index = tbl_SPGioHang.getSelectedRow();
        String maSP = tbl_SPGioHang.getValueAt(index, 0).toString();
        giohang.removeProduct(maSP);
        fillToTable();
    }
    
    public void totalPrice() {
        double total = 0d;
        Set<String> keyset = giohang.getChiTietGioHang().keySet();
        for (String key : keyset) {
            SanPham sp = giohang.getChiTietGioHang().get(key).getSp();
            int soluong = giohang.getChiTietGioHang().get(key).getSoluong();
            total += sp.getDongia() * soluong;
        }
        txt_ThanhTien.setText(formatter.format(new BigDecimal(total)) + " VND");
    }
    
    public void loadCart() {
        DefaultListModel model = new DefaultListModel();
        Set<String> keyset = listCart.keySet();
        for(String key : keyset) {
            model.addElement(key);
        }
        lst_GioHang.setModel(model);
    }
    
    public void saveCart() {
        Timestamp timestamp = new Timestamp(System.currentTimeMillis());
        if(giohang.getChiTietGioHang().size() < 1) {
            listCart.remove(String.valueOf(giohang.getId()));
        } else {
             if(listCart.containsValue(giohang)) {
                 listCart.replace(String.valueOf(giohang.getId()), giohang);
             } else {
                 giohang.setId(timestamp.getTime());
                 listCart.put(String.valueOf(giohang.getId()), giohang);
             }
        }
    }
    
    public void viewCart() {
        String idCart = lst_GioHang.getSelectedValue();
        giohang = listCart.get(idCart);
        giohang.setId(Long.parseLong(idCart));
        fillToTable();
        totalPrice();
    }
    
    public void removeCart() {
        String IdCart = lst_GioHang.getSelectedValue();
        giohang = listCart.get(IdCart);
        if(listCart.containsValue(giohang)) {
            listCart.remove(IdCart);
            loadCart();
            giohang = new GioHang();
            fillToTable();
            totalPrice();
        }
    }
 
    
