    private void btninsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btninsertActionPerformed
        // TODO add your handling code here:
        list.add(new Library(Integer.parseInt(txtid.getText()),txttitle.getText(),txtauthor.getText(),Integer.parseInt(txtyear.getText()),Integer.parseInt(txtpages.getText())));
        addRow();
    }//GEN-LAST:event_btninsertActionPerformed

    private void btnupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnupdateActionPerformed
        // TODO add your handling code here:
        int index = table.getSelectedRow();
        update(index);
        addRow();
    }//GEN-LAST:event_btnupdateActionPerformed

    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
        // TODO add your handling code here:
        model = (DefaultTableModel) table.getModel();
        int index = table.getSelectedRow();
        if (index < 0) {
            JOptionPane.showMessageDialog(this, "You have not selected the object to delete");
        } else {
           
            list.remove(index);
            JOptionPane.showMessageDialog(this, "Xoá thành công");
            addRow();
        }
        
    }//GEN-LAST:event_btndeleteActionPerformed
public void table(){ 
    model = new DefaultTableModel();
    model.setColumnIdentifiers(header);
    table.setModel(model);
}
public void update(int index){ 
    Library la = list.get(index);
    la.setId(Integer.parseInt(txtid.getText()));
    la.setYear(Integer.parseInt(txtyear.getText()));
    la.setPages(Integer.parseInt(txtpages.getText()));
    JOptionPane.showMessageDialog(this, "Cập nhật thành công");
}
public void addRow(){ 
    model.setRowCount(0);
    for(Library l : list){ 
        model.addRow(new Object[]{l.getId(),l.getTitle(),l.getAuthor(),l.getYear(),l.getPages()});
    }
    model.fireTableDataChanged();
}

